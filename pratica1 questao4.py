# Faça um Programa que peça dois números e imprima o maior deles.

num1 = int(input("Digite um número: "))

num2 = int(input("Digite um número: "))

if num1 > num2:
    print("O maior número entre os que foram inseridos é: ",num1)

else:
    print("O maior número entre os que foram inseridos é: ",num2)